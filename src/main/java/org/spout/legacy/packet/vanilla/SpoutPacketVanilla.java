package org.spout.legacy.packet.vanilla;

import org.spout.legacyapi.packet.PacketVanilla;

import net.minecraft.server.Packet;

/**
 * 
 */
public class SpoutPacketVanilla<T extends Packet> implements PacketVanilla {

	protected T packet;

	/**
	 * 
	 * @param packet
	 */
	public void setPacket(T packet) {
		this.packet = packet;
	}

	/**
	 * 
	 * @return
	 */
	public T getPacket() {
		return packet;
	}

	/**
	 * {@inhericDoc}
	 */
	@Override
	public int getId() {
		return packet.k();
	}

}
